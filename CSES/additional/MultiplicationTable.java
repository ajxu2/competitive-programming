import java.util.*;
import java.io.*;

public class MultiplicationTable {
    static FastIO io = new FastIO();
    public static void main(String[] args) throws IOException {
        long n = io.nextInt();
        long l = 1, r = n * n + 1;
        long target = n * n + 1 >> 1;
        while (l < r) {
            long mid = l + r >> 1;
            long cnt = 0;
            for (int i = 1; i <= n; i++) cnt += Math.min(n, mid / i);
            if (cnt >= target) r = mid;
            else l = mid + 1;
        }
        io.println(l);
        io.close();
    }
}

